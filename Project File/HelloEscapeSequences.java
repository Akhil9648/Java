public class HelloEscapeSequences {
public static void main(String[] args) {
System.out.println("Hello, Java World from Eclipse!");
System.out.println("Newline character:\\n creates a new line\nThis is a new line.");
System.out.println("Tab character:\\t adds a tab\tSee the tab space.");
System.out.println("Backslash character:\\\\ displays a backslash \\");
System.out.println("Double quote character:\\\" displays a double quote \"");
System.out.println("Single quote character:\\' displays a single quote \'");
System.out.println("Carriage return character:\\r will return to start of the line\rOverwrite");
}
}